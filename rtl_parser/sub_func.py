def find_driver(line_content):
	signal_name	= "";
	is_driver	= 0;
	line_value	= line_content;

	##	-------------------------------------------------------------------------------------
	##	如果有 < ，说明是赋值语句，提取之前的信息
	##	-------------------------------------------------------------------------------------
	try:
	    index_value = line_value.index("<")
	except ValueError:
	    index_value = -1
	if(index_value!=-1):
		line_value	= line_value[0:index_value];
		is_driver	= 1;
	##	-------------------------------------------------------------------------------------
	##	如果有 = ，说明是赋值语句，提取之前的信息
	##	-------------------------------------------------------------------------------------
	try:
	    index_value = line_value.index("=")
	except ValueError:
	    index_value = -1
	if(index_value!=-1):
		line_value	= line_value[0:index_value];
		is_driver	= 1;

	##	-------------------------------------------------------------------------------------
	##	如果有 : ，说明是case赋值语句，删掉
	##	先截取 = ，再截取 :。否则 assign 语句就会解析错误
	##	-------------------------------------------------------------------------------------
	try:
	    index_value = line_value.index(":")
	except ValueError:
	    index_value = -1
	if(index_value!=-1): line_value	= line_value[index_value+1:len(line_value)];

	##	-------------------------------------------------------------------------------------
	##	如果有 ) ，说明有条件语句，删掉
	##	-------------------------------------------------------------------------------------
	try:
	    index_value = line_value.index(")")
	except ValueError:
	    index_value = -1
	if(index_value!=-1): line_value	= line_value[index_value+1:len(line_value)];

	##	-------------------------------------------------------------------------------------
	##	如果有 ( ，说明是半条 条件语句，此时不是赋值语句
	##	-------------------------------------------------------------------------------------
	try:
	    index_value = line_value.index("(")
	except ValueError:
	    index_value = -1
	if(index_value!=-1): is_driver	= 0;

	##	-------------------------------------------------------------------------------------
	##	如果是赋值语句，那么要提取信号名称
	##	-------------------------------------------------------------------------------------
	if(is_driver==1):
#		print("is_driver")
		##	-------------------------------------------------------------------------------------
		##	合并空格
		##	tab 转换为空格
		##	去掉行首行尾的空格符
		##	提取信号名称
		##	-------------------------------------------------------------------------------------
		line_value	= line_value.replace("\t"," ");
		line_value	= line_value.strip();
		line_value	= line_value.split();
		signal_name	= line_value[len(line_value)-1];
	return signal_name

